package com.entity;

public class Course {
    private int couid;
    private String counam;
    private int coutimes;
    private String couroom;
    private String courtea;

    public int getCouid() {
        return couid;
    }

    public void setCouid(int couid) {
        this.couid = couid;
    }

    public String getCounam() {
        return counam;
    }

    public void setCounam(String counam) {
        this.counam = counam;
    }

    public int getCoutimes() {
        return coutimes;
    }

    public void setCoutimes(int coutimes) {
        this.coutimes = coutimes;
    }

    public String getCouroom() {
        return couroom;
    }

    public void setCouroom(String couroom) {
        this.couroom = couroom;
    }

    public String getCourtea() {
        return courtea;
    }

    public void setCourtea(String courtea) {
        this.courtea = courtea;
    }
}
